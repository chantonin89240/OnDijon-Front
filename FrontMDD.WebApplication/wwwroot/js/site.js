var map = L.map('map').setView([47.32665, 5.04497], 15);

L.tileLayer('https://tile.openstreetmap.org/{z}/{x}/{y}.png', {
    maxZoom: 19,
    attribution: '&copy; <a href="http://www.openstreetmap.org/copyright">OpenStreetMap</a>'
}).addTo(map);

var marker = L.marker([47.3351, 5.0538]).addTo(map);
marker.bindPopup("le DIIAGE").openPopup();